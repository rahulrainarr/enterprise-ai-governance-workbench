from __future__ import annotations

from pathlib import Path

TEMPLATES = {"readiness": "AI_READINESS_REPORT.md", "governance": "GOVERNANCE_GAP_REPORT.md", "evidence": "EXECUTIVE_EVIDENCE_PACK.md", "risk": "RISK_REGISTER.md", "roadmap": "ROADMAP_30_60_90.md"}


def build_markdown_report(report_type: str, context: dict, output_dir: str | Path) -> Path:
    output = Path(output_dir)
    output.mkdir(parents=True, exist_ok=True)
    template_name = TEMPLATES.get(report_type, TEMPLATES["readiness"])
    path = output / template_name
    lines = [
        f"# {template_name.removesuffix('.md').replace('_', ' ').title()}",
        "",
        "Generated by Private Enterprise Knowledge & Governance Workbench.",
        "",
        "## Executive Summary",
        str(context.get("executive_summary", "Not specified.")),
        "",
        "## Evidence Pack",
        str(context.get("evidence", "No evidence supplied.")),
        "",
        "## Governance Gaps",
        str(context.get("governance_gaps", "No gaps supplied.")),
        "",
        "## Risk Register",
        str(context.get("risks", "No risks supplied.")),
        "",
        "## Recommended Roadmap",
        str(context.get("roadmap", "No roadmap supplied.")),
    ]
    path.write_text("\n".join(lines), encoding="utf-8")
    return path
